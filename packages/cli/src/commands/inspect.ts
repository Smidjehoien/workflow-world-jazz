import { Args, Flags } from '@oclif/core';
import { BaseCommand } from '../base.js';
import { logWarn, setJsonMode } from '../lib/config/log.js';
import {
  listEvents,
  listRuns,
  listSteps,
  printStream,
  showRun,
  showStep,
} from '../lib/inspect/output.js';
import { getWorld } from '../lib/inspect/world.js';

export default class Inspect extends BaseCommand {
  static description = 'Inspect runs, steps, streams, or events';

  static aliases = ['i'];

  static examples = ['$ workflow inspect runs'];

  static args = {
    resource: Args.string({
      description: 'what to inspect: run(s) | step(s) | stream(s) | event(s)',
      required: true,
      options: [
        'r',
        'run',
        'runs',
        's',
        'step',
        'steps',
        'e',
        'event',
        'events',
        'st',
        'stream',
        'streams',
      ],
    }),
    id: Args.string({
      description:
        'ID of the run (for runs) or run-id (for steps/streams/events)',
      required: false,
    }),
    subId: Args.string({
      description:
        'optional step-id or stream-id when inspecting a specific item',
      required: false,
      dependsOn: ['id'],
    }),
  } as const;

  static flags = {
    json: Flags.boolean({
      description: 'output JSON instead of human-readable tables/streams',
      required: false,
      char: 'j',
      default: false,
    }),
    backend: Flags.string({
      description: 'backend to inspect',
      required: false,
      options: ['vercel', 'embedded'],
      char: 'b',
      default: 'embedded',
      env: 'WORKFLOW_BACKEND',
    }),
    env: Flags.string({
      description: 'environment to inspect',
      required: false,
      options: ['production', 'preview'],
      default: 'production',
      char: 'e',
      dependsOn: ['backend'],
      env: 'WORKFLOW_ENV',
    }),
    authToken: Flags.string({
      description:
        'Authentication token to be passed to the backend. If not provided and the backend is vercel, will fall back to the token generated by `wf login`.',
      required: false,
      char: 'a',
      dependsOn: ['backend'],
      env: 'WORKFLOW_AUTH_TOKEN',
    }),
    project: Flags.string({
      description:
        'If backend is vercel, the vercel project to authenticate against',
      required: false,
      dependsOn: ['backend'],
      env: 'VERCEL_PROJECT',
    }),
    team: Flags.string({
      description:
        'If backend is vercel, the vercel team to authenticate against',
      required: false,
      dependsOn: ['backend'],
      env: 'VERCEL_TEAM',
    }),
    hostUrl: Flags.string({
      description: 'The host URL that the target backend is running on',
      required: false,
      dependsOn: ['backend'],
      env: 'WORKFLOW_HOST_URL',
      char: 'h',
    }),
  } as const;

  public async run(): Promise<void> {
    const { args, flags } = await this.parse(Inspect);
    setJsonMode(Boolean(flags.json));

    logWarn('=================================================');
    logWarn('Workflow CLI is in alpha, expect things to break!');
    logWarn('=================================================');

    if (flags.env !== 'production') {
      throw new Error('Preview environments are not supported yet');
    }

    const world = await getWorld({
      world: flags.backend as 'embedded' | 'vercel',
      env: flags.env as 'production' | 'preview',
      authToken: flags.authToken,
      projectId: flags.project,
      teamId: flags.team,
      hostUrl: flags.hostUrl,
    });

    const normalizeResource = (
      value?: string
    ): 'run' | 'step' | 'stream' | 'event' | undefined => {
      if (!value) return undefined;
      const v = value.toLowerCase();
      if (v.startsWith('r')) return 'run';
      if (v.startsWith('e')) return 'event';
      if (v.startsWith('ste')) return 'step';
      if (v.startsWith('str')) return 'stream';
      return undefined;
    };

    const resource = normalizeResource(args.resource);
    if (!resource) {
      this.logInfo(
        'You must specify what to inspect: runs(s), step(s), stream(s), or event(s).\n'
      );
      this.logInfo('Examples:');
      for (const ex of Inspect.examples) this.logInfo(`  ${ex}`);
      this.logInfo('');
      this.logInfo('Aliases:');
      this.logInfo("- Command: 'inspect' can be shortened to 'i'");
      this.logInfo(
        '- Resources: plural forms are accepted: runs, steps, streams'
      );
      return;
    }

    const id = args.id;
    const subId = args.subId;

    if (!flags.json) this.logInfo('');

    if (resource === 'run') {
      if (id) {
        await showRun(world, id, { json: flags.json });
      } else {
        await listRuns(world, { json: flags.json });
      }
      return;
    }

    if (resource === 'step') {
      if (!id) {
        this.logError(
          'Missing required argument: run-id. Usage: inspect steps <run-id> [<step-id>]'
        );
        return;
      }
      if (subId) {
        await showStep(world, id, subId, { json: flags.json });
      } else {
        await listSteps(world, id, { json: flags.json });
      }
      return;
    }

    if (resource === 'stream') {
      if (!id) {
        this.logError(
          'Missing required argument: run-id. Usage: inspect streams <run-id> [<stream-id>]'
        );
        return;
      }
      await printStream(world, id, { json: flags.json });
      return;
    }

    if (resource === 'event') {
      if (!id) {
        this.logError(
          'Missing required argument: run-id. Usage: inspect events <run-id>'
        );
        return;
      }
      if (subId) {
        this.logError(
          'Sub-ID is not supported for events. Usage: inspect events <run-id>'
        );
        return;
      }
      await listEvents(world, id, { json: flags.json });
      return;
    }
  }
}
